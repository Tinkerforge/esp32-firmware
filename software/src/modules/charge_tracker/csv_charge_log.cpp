#include "csv_charge_log.h"


String chargeRecordFilename(uint32_t i)
{
    char buf[64];
    StringWriter sw(buf, sizeof(buf));

    sw.printf(CHARGE_RECORD_FOLDER "/charge-record-%lu.bin", i);

    return String(buf, sw.getLength());
}
